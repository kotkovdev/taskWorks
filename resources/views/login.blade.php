    $(function() {
      $('#login-form').submit(function(e) {
        userCredentials = {
          email: $('#email').val(),
          password: $('#password').val()
        };
        $.ajax({
          url: "/api/login",
          type: "post",
          async: false,
          data: JSON.stringify(userCredentials),
          success: (response) => {
            if (response.api_token != undefined) {
              sessionStorage.setItem('api_token', response.api_token);
              this.off('submit');
              this.submit();
            }
            if (response.errors) {
              $('#error-message').text(response.errors).show();
            }
          }
        });
      });
    });
  </script>
